class ArithmeticDemo
{
public static void main(String args[])
{
int i=10,j=20;
System.out.println("sum of numbers"+(i+j));
System.out.println("substraction of numbers" +(i-j));
System.out.println("product of numbers" +(i*j));
System.out.println("Division of numbers" +(i/j));
System.out.println("remainder of number" +(i%j));
}
}


